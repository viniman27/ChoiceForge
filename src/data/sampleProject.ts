function graphWords(graph: SceneGraph): number {
  return graph.nodes
    .flatMap((node) => [node.body, node.prompt, ...(node.options?.map((option) => option.text) ?? []), ...(node.fakeOptions?.map((option) => option.text) ?? [])])
    .join(" ")
    .split(/\s+/)
    .filter(Boolean)
    .length;
}

